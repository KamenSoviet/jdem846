package us.wthr.jdem846.render.mapprojection;

public class MapPoint
{
	
	public double row;
	public double column;
	
	public MapPoint()
	{
		
	}

	public double getRow()
	{
		return row;
	}

	public void setRow(double row)
	{
		this.row = row;
	}

	public double getColumn()
	{
		return column;
	}

	public void setColumn(double column)
	{
		this.column = column;
	}
	
	
	
}
