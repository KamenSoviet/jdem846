package us.wthr.jdem846.geom;

public class Bounds
{
	
	public double x;
	public double y;
	public double width;
	public double height;
	
	
	public Bounds(double x, double y, double width, double height)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
	
	
}
